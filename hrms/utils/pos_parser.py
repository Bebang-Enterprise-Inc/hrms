def parse_daily_sales_revenue(file_content: bytes) -> Dict[str, Any]:
    """
    Parse MOSAIC Daily Sales Revenue - Summary export.

    Contains per-transaction revenue with payment breakdown:
    - Net/Gross sales per transaction
    - Payment type per transaction
    - Order type and service channel

    Args:
        file_content: Excel file bytes

    Returns:
        Dictionary with extracted data:
        {
            "metadata": {...},
            "transactions": [...],
            "totals": {
                "gross_sales": 12345.00,
                "net_sales": 11000.00,
                "by_payment_type": {...},
                "by_order_type": {...}
            },
            "success": True
        }
    """
    try:
        df = pd.read_excel(io.BytesIO(file_content), header=None)
        metadata = _extract_metadata(df)

        # Read data starting from row 10 (header is row 9)
        # Columns: Location, Date, OR No, Bill No, Bill Time, Settle Date,
        # Settle Time, Manual SI No., Terminal Id, Cashier, Discount Amount,
        # Vat Adjustment, Tax Amount, VAT, Service Charge Amount, Net Sales,
        # Gross Sales, Gc Excess, Order Type, Service Channel, Payment Type,
        # More Details, Short Order Id, Customer Tin, Customer Name, Customer Address,
        # Customer Business Style

        data_df = pd.read_excel(io.BytesIO(file_content), header=9)
        data_df.columns = data_df.columns.str.strip()

        transactions = []
        gross_total = 0.0
        net_total = 0.0
        payment_totals = {}
        order_type_totals = {}

        for _, row in data_df.iterrows():
            # Skip empty rows and summary rows
            if pd.isna(row.get("Bill No")):
                continue

            tx = {
                "location": str(row.get("Location", "")) if pd.notna(row.get("Location")) else None,
                "date": _parse_date(row.get("Date")),
                "or_no": _safe_int(row.get("OR No")),
                "bill_no": _safe_int(row.get("Bill No")),
                "bill_time": str(row.get("Bill Time", "")) if pd.notna(row.get("Bill Time")) else None,
                "terminal_id": str(row.get("Terminal Id", "")) if pd.notna(row.get("Terminal Id")) else None,
                "cashier": str(row.get("Cashier", "")) if pd.notna(row.get("Cashier")) else None,
                "discount_amount": _safe_float(row.get("Discount Amount")),
                "vat_adjustment": _safe_float(row.get("Vat Adjustment")),
                "tax_amount": _safe_float(row.get("Tax Amount")),
                "vat": _safe_float(row.get("VAT")),
                "net_sales": _safe_float(row.get("Net Sales")),
                "gross_sales": _safe_float(row.get("Gross Sales")),
                "gc_excess": _safe_float(row.get("Gc Excess")),
                "order_type": str(row.get("Order Type", "")) if pd.notna(row.get("Order Type")) else None,
                "service_channel": str(row.get("Service Channel", "")) if pd.notna(row.get("Service Channel")) else None,
                "payment_type": str(row.get("Payment Type", "")) if pd.notna(row.get("Payment Type")) else None,
            }

            transactions.append(tx)

            # Accumulate totals
            gross_total += tx["gross_sales"]
            net_total += tx["net_sales"]

            # By payment type
            payment_type = tx["payment_type"] or "Unknown"
            if payment_type not in payment_totals:
                payment_totals[payment_type] = 0.0
            payment_totals[payment_type] += tx["gross_sales"]

            # By order type
            order_type = tx["order_type"] or "Unknown"
            if order_type not in order_type_totals:
                order_type_totals[order_type] = 0.0
            order_type_totals[order_type] += tx["gross_sales"]

        return {
            "metadata": metadata,
            "transactions": transactions,
            "totals": {
                "gross_sales": gross_total,
                "net_sales": net_total,
                "transaction_count": len(transactions),
                "by_payment_type": payment_totals,
                "by_order_type": order_type_totals
            },
            "success": True
        }

    except Exception as e:
        return {"success": False, "error": str(e)}


