def parse_product_mix(file_content: bytes) -> Dict[str, Any]:
    """
    Parse MOSAIC Product Mix export.

    Contains SKU-level sales data for BOM deduction:
    - Product/item codes
    - Quantities sold
    - Per-item pricing
    - Category breakdown

    Args:
        file_content: Excel file bytes

    Returns:
        Dictionary with extracted data:
        {
            "metadata": {...},
            "items": [...],
            "totals": {
                "total_qty": 357,
                "gross_sales": 65473.93,
                "net_sales": 55587.54,
                "by_category": {...}
            },
            "success": True
        }
    """
    try:
        df = pd.read_excel(io.BytesIO(file_content), header=None)
        metadata = _extract_metadata(df)

        # Product Mix has header at row 11
        # Columns: Location Name, Product Group, SKU/Item Code, Item Name,
        # Open Item, Forced Modifier Name, Unforced Modifier Name,
        # Per Price (item price w/o VAT), Total Forced Modifier Price with VAT,
        # Item Qty, Discount Qty, Complimentary/Non-Revenue Qty, Gross Sales,
        # Discount Amount, VAT Adjustment, VAT, Service Charge, Net Sales

        data_df = pd.read_excel(io.BytesIO(file_content), header=11)
        data_df.columns = data_df.columns.str.strip()

        items = []
        category_totals = {}
        total_qty = 0
        gross_total = 0.0
        net_total = 0.0

        for _, row in data_df.iterrows():
            # Skip empty rows and Grand Totals row
            location = row.get("Location Name")
            if pd.isna(location) or str(location).strip() in ["Grand Totals:", ""]:
                continue

            item = {
                "location": str(location) if pd.notna(location) else None,
                "product_group": str(row.get("Product Group", "")) if pd.notna(row.get("Product Group")) else None,
                "sku_code": str(row.get("SKU/Item Code", "")) if pd.notna(row.get("SKU/Item Code")) else None,
                "item_name": str(row.get("Item Name", "")) if pd.notna(row.get("Item Name")) else None,
                "is_open_item": str(row.get("Open Item", "No")).upper() == "YES",
                "unit_price": _safe_float(row.get("Per Price (item price w/o VAT)")),
                "qty_sold": _safe_int(row.get("Item Qty")),
                "qty_discount": _safe_int(row.get("Discount Qty")),
                "qty_complimentary": _safe_int(row.get("Complimentary/Non-Revenue Qty")),
                "gross_sales": _safe_float(row.get("Gross Sales")),
                "discount_amount": _safe_float(row.get("Discount Amount")),
                "vat_adjustment": _safe_float(row.get("VAT Adjustment")),
                "vat": _safe_float(row.get("VAT")),
                "net_sales": _safe_float(row.get("Net Sales")),
            }

            items.append(item)

            # Accumulate totals
            total_qty += item["qty_sold"]
            gross_total += item["gross_sales"]
            net_total += item["net_sales"]

            # By category
            category = item["product_group"] or "Uncategorized"
            if category not in category_totals:
                category_totals[category] = {"qty": 0, "gross_sales": 0.0, "net_sales": 0.0}
            category_totals[category]["qty"] += item["qty_sold"]
            category_totals[category]["gross_sales"] += item["gross_sales"]
            category_totals[category]["net_sales"] += item["net_sales"]

        return {
            "metadata": metadata,
            "items": items,
            "totals": {
                "total_qty": total_qty,
                "gross_sales": gross_total,
                "net_sales": net_total,
                "item_count": len(items),
                "by_category": category_totals
            },
            "success": True
        }

    except Exception as e:
        return {"success": False, "error": str(e)}


# ==============================================================================
# UNIFIED EXTRACTION FUNCTION
# ==============================================================================

def extract_all_pos_data(
    sales_summary_content: bytes = None,
    transaction_report_content: bytes = None,
    discount_report_content: bytes = None,
    daily_sales_revenue_content: bytes = None,
    product_mix_content: bytes = None
) -> Dict[str, Any]:
    """
    Extract data from all 5 POS file types.

    Args:
        sales_summary_content: Sales Summary Excel bytes
        transaction_report_content: Transaction Report Excel bytes
        discount_report_content: Discount Report Excel bytes
        daily_sales_revenue_content: Daily Sales Revenue Excel bytes
        product_mix_content: Product Mix Excel bytes

    Returns:
        Unified dictionary with all extracted data:
        {
            "sales_summary": {...},
            "transaction_report": {...},
            "discount_report": {...},
            "daily_sales_revenue": {...},
            "product_mix": {...},
            "consolidated": {
                "date": "2026-01-30",
                "store": "...",
                "gross_sales": 65473.93,
                "net_sales": 55587.54,
                "vat": 5575.48,
                "beginning_si": 45788,
                "ending_si": 46000,
                "transaction_count": 212,
                "eod_counter": 212,
                "discount_pwd": 500.00,
                "discount_senior": 750.00,
                "discount_other": 100.00,
                "total_discount": 1350.00,
                "by_payment_type": {...},
                "by_category": {...}
            },
            "success": True
        }
    """
    result = {
        "sales_summary": None,
        "transaction_report": None,
        "discount_report": None,
        "daily_sales_revenue": None,
        "product_mix": None,
        "consolidated": {},
        "success": True,
        "errors": []
    }

    # Parse each file if provided
    if sales_summary_content:
        result["sales_summary"] = parse_sales_summary(sales_summary_content)
        if not result["sales_summary"]["success"]:
            result["errors"].append(f"Sales Summary: {result['sales_summary'].get('error')}")

    if transaction_report_content:
        result["transaction_report"] = parse_transaction_report(transaction_report_content)
        if not result["transaction_report"]["success"]:
            result["errors"].append(f"Transaction Report: {result['transaction_report'].get('error')}")

    if discount_report_content:
        result["discount_report"] = parse_discount_report(discount_report_content)
        if not result["discount_report"]["success"]:
            result["errors"].append(f"Discount Report: {result['discount_report'].get('error')}")

    if daily_sales_revenue_content:
        result["daily_sales_revenue"] = parse_daily_sales_revenue(daily_sales_revenue_content)
        if not result["daily_sales_revenue"]["success"]:
            result["errors"].append(f"Daily Sales Revenue: {result['daily_sales_revenue'].get('error')}")

    if product_mix_content:
        result["product_mix"] = parse_product_mix(product_mix_content)
        if not result["product_mix"]["success"]:
            result["errors"].append(f"Product Mix: {result['product_mix'].get('error')}")

    # Build consolidated summary (primarily from Sales Summary)
    consolidated = {}

    if result["sales_summary"] and result["sales_summary"]["success"]:
        ss = result["sales_summary"]["summary"]
        consolidated.update({
            "date": ss.get("date"),
            "gross_sales": ss.get("gross_sales"),
            "net_sales": ss.get("net_sales"),
            "vat": ss.get("vat"),
            "vatable_sales": ss.get("vatable_sales"),
            "vat_exempt_sales": ss.get("vat_exempt_sales"),
            "zero_rated_sales": ss.get("zero_rated_sales"),
            "beginning_si": ss.get("beginning_si"),
            "ending_si": ss.get("ending_si"),
            "transaction_count": ss.get("transaction_count"),
            "eod_counter": ss.get("eod_counter"),
            "discount_pwd": ss.get("discount_pwd"),
            "discount_senior": ss.get("discount_senior"),
            "discount_other": ss.get("discount_other"),
            "vat_adjustment": ss.get("vat_adjustment"),
            "delivery_fee": ss.get("delivery_fee"),
            "gc_excess": ss.get("gc_excess"),
            "returns": ss.get("returns"),
            "void_count": ss.get("void_count"),
        })

        if result["sales_summary"].get("metadata"):
            consolidated["store"] = result["sales_summary"]["metadata"].get("location")
            consolidated["tin"] = result["sales_summary"]["metadata"].get("tin")

    # Add payment type breakdown from Transaction Report
    if result["transaction_report"] and result["transaction_report"]["success"]:
        consolidated["by_payment_type"] = result["transaction_report"]["totals"].get("by_payment_type", {})

    # Add category breakdown from Product Mix
    if result["product_mix"] and result["product_mix"]["success"]:
        consolidated["by_category"] = result["product_mix"]["totals"].get("by_category", {})
        consolidated["total_items_sold"] = result["product_mix"]["totals"].get("total_qty", 0)

    # Add discount breakdown from Discount Report
    if result["discount_report"] and result["discount_report"]["success"]:
        consolidated["discount_breakdown"] = result["discount_report"]["totals"].get("by_type", {})
        consolidated["discount_count"] = result["discount_report"]["totals"].get("discount_count", 0)

    # Calculate total discounts
    consolidated["total_discount"] = (
        consolidated.get("discount_pwd", 0) +
        consolidated.get("discount_senior", 0) +
        consolidated.get("discount_other", 0)
    )

    result["consolidated"] = consolidated

    if result["errors"]:
        result["success"] = False

    return result
