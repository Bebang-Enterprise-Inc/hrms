def parse_discount_report(file_content: bytes) -> Dict[str, Any]:
    """
    Parse MOSAIC Discount Report export.

    Contains discount breakdown by type:
    - SC (Senior Citizen)
    - PWD
    - Promo discounts
    - VAT adjustments

    Args:
        file_content: Excel file bytes

    Returns:
        Dictionary with extracted data:
        {
            "metadata": {...},
            "discounts": [...],
            "totals": {
                "total_discount": 1500.00,
                "total_vat_adjustment": 100.00,
                "by_type": {
                    "Senior Citizen": 750.00,
                    "PWD": 500.00,
                    ...
                }
            },
            "success": True
        }
    """
    try:
        df = pd.read_excel(io.BytesIO(file_content), header=None)
        metadata = _extract_metadata(df)

        # Find header row (row 9 in standard format)
        # Columns: Pos Date, Terminal Id, Location, Bill No, Discount Amount,
        # Vat Adjustment, VAT, Service Charge Amount, Sub Total, Other Charges,
        # Final Amount, Discount Name, Discount Type, Reference Number, Name,
        # Requested By, Time, Date

        # Read data starting from row 10
        data_df = pd.read_excel(io.BytesIO(file_content), header=9)
        data_df.columns = data_df.columns.str.strip()

        discounts = []
        discount_by_type = {}
        total_discount = 0.0
        total_vat_adjustment = 0.0

        for _, row in data_df.iterrows():
            # Skip empty rows
            if pd.isna(row.get("Bill No")):
                continue

            discount = {
                "pos_date": _parse_date(row.get("Pos Date")),
                "terminal_id": str(row.get("Terminal Id", "")) if pd.notna(row.get("Terminal Id")) else None,
                "location": str(row.get("Location", "")) if pd.notna(row.get("Location")) else None,
                "bill_no": _safe_int(row.get("Bill No")),
                "discount_amount": _safe_float(row.get("Discount Amount")),
                "vat_adjustment": _safe_float(row.get("Vat Adjustment")),
                "vat": _safe_float(row.get("VAT")),
                "subtotal": _safe_float(row.get("Sub Total")),
                "final_amount": _safe_float(row.get("Final Amount")),
                "discount_name": str(row.get("Discount Name", "")) if pd.notna(row.get("Discount Name")) else None,
                "discount_type": str(row.get("Discount Type", "")) if pd.notna(row.get("Discount Type")) else None,
                "reference_number": str(row.get("Reference Number", "")) if pd.notna(row.get("Reference Number")) else None,
                "customer_name": str(row.get("Name", "")) if pd.notna(row.get("Name")) else None,
            }

            discounts.append(discount)

            # Accumulate totals
            total_discount += discount["discount_amount"]
            total_vat_adjustment += discount["vat_adjustment"]

            discount_name = discount["discount_name"] or "Other"
            if discount_name not in discount_by_type:
                discount_by_type[discount_name] = 0.0
            discount_by_type[discount_name] += discount["discount_amount"]

        return {
            "metadata": metadata,
            "discounts": discounts,
            "totals": {
                "total_discount": total_discount,
                "total_vat_adjustment": total_vat_adjustment,
                "by_type": discount_by_type,
                "discount_count": len(discounts)
            },
            "success": True
        }

    except Exception as e:
        return {"success": False, "error": str(e)}


