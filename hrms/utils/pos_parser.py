def parse_transaction_report(file_content: bytes) -> Dict[str, Any]:
    """
    Parse MOSAIC Transaction Report export.

    Contains per-transaction details:
    - Bill/Receipt numbers
    - Payment types (Cash, GCash, Card, etc.)
    - Service type (Pickup, Delivery)
    - Tax and totals per transaction

    Args:
        file_content: Excel file bytes

    Returns:
        Dictionary with extracted data:
        {
            "transactions": [...],
            "totals": {
                "gross_sales": 12345.00,
                "net_sales": 11000.00,
                "tax_total": 1200.00,
                "transaction_count": 50,
                "by_payment_type": {
                    "Cash": 5000.00,
                    "GCash": 3000.00,
                    ...
                }
            },
            "success": True
        }
    """
    try:
        df = pd.read_excel(io.BytesIO(file_content), header=0)

        # Clean column names
        df.columns = df.columns.str.strip()

        transactions = []
        payment_totals = {}
        gross_total = 0.0
        tax_total = 0.0

        for _, row in df.iterrows():
            # Skip summary rows
            if pd.isna(row.get("Bill #")):
                continue

            tx = {
                "bill_no": _safe_int(row.get("Bill #")),
                "receipt_no": _safe_int(row.get("Receipt #")),
                "location": str(row.get("Location", "")) if pd.notna(row.get("Location")) else None,
                "status": str(row.get("Status", "")) if pd.notna(row.get("Status")) else None,
                "service_type": str(row.get("Service Type", "")) if pd.notna(row.get("Service Type")) else None,
                "service_channel": str(row.get("Service Channel", "")) if pd.notna(row.get("Service Channel")) else None,
                "payment_type": str(row.get("Payment Type", "")) if pd.notna(row.get("Payment Type")) else None,
                "guest_count": _safe_int(row.get("Guest Count", 1)),
                "tax": _safe_float(row.get("Tax")),
                "subtotal": _safe_float(row.get("Sub-total")),
                "total": _safe_float(row.get("Total")),
                "gross_sales": _safe_float(row.get("Gross Sales")),
                "date": _parse_date(row.get("Date")),
            }

            transactions.append(tx)

            # Accumulate totals
            gross_total += tx["gross_sales"]
            tax_total += tx["tax"]

            payment_type = tx["payment_type"] or "Unknown"
            if payment_type not in payment_totals:
                payment_totals[payment_type] = 0.0
            payment_totals[payment_type] += tx["total"]

        return {
            "transactions": transactions,
            "totals": {
                "gross_sales": gross_total,
                "tax_total": tax_total,
                "transaction_count": len(transactions),
                "by_payment_type": payment_totals
            },
            "success": True
        }

    except Exception as e:
        return {"success": False, "error": str(e)}


