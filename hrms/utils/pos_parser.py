"""
POS File Parser for MOSAIC POS System

Parses 5 export file types from MOSAIC POS:
1. Sales Summary - Z-Reading equivalent (invoice numbers, totals, discounts)
2. Transaction Report - Per-transaction detail
3. Discount Report - SC/PWD and promo discount breakdown
4. Daily Sales Revenue - Per-transaction revenue with payment types
5. Product Mix - SKU-level sales for BOM deduction

All parsers return structured dictionaries that can be used for:
- Auto-populating closing report fields
- Cross-validating against OCR-extracted Z-Reading data
- Inventory BOM deduction calculations
"""

import io
import pandas as pd
from datetime import datetime
from typing import Optional, Dict, Any, List, Tuple


# ==============================================================================
# COMMON UTILITIES
# ==============================================================================

def _extract_metadata(df: pd.DataFrame) -> Dict[str, Any]:
    """
    Extract common metadata from MOSAIC header rows.
    Header format (rows 0-7):
        0: POS: Mosaic POS v1.5
        1: Account Name: BEBANG
        2: Location: [Full address]
        3: TIN: 649154841-00001
        4: From Date: 2026-01-30
        5: To Date: 2026-01-30
        6: Generated Date: 2026-01-31
        7: Generated by: [username]
    """
    metadata = {}
    try:
        metadata["pos_version"] = str(df.iloc[0, 1]) if pd.notna(df.iloc[0, 1]) else None
        metadata["account_name"] = str(df.iloc[1, 1]) if pd.notna(df.iloc[1, 1]) else None
        metadata["location"] = str(df.iloc[2, 1]) if pd.notna(df.iloc[2, 1]) else None
        metadata["tin"] = str(df.iloc[3, 1]) if pd.notna(df.iloc[3, 1]) else None

        # Parse dates
        from_date = df.iloc[4, 1]
        to_date = df.iloc[5, 1]
        generated_date = df.iloc[6, 1]

        if pd.notna(from_date):
            metadata["from_date"] = _parse_date(from_date)
        if pd.notna(to_date):
            metadata["to_date"] = _parse_date(to_date)
        if pd.notna(generated_date):
            metadata["generated_date"] = _parse_date(generated_date)

        metadata["generated_by"] = str(df.iloc[7, 1]) if pd.notna(df.iloc[7, 1]) else None
    except Exception:
        pass

    return metadata


def _parse_date(value) -> Optional[str]:
    """Parse various date formats to YYYY-MM-DD string."""
    if pd.isna(value):
        return None

    if isinstance(value, datetime):
        return value.strftime("%Y-%m-%d")

    if isinstance(value, str):
        # Try common formats
        for fmt in ["%Y-%m-%d", "%b-%d-%Y", "%d-%b-%Y", "%m/%d/%Y", "%d/%m/%Y"]:
            try:
                return datetime.strptime(value, fmt).strftime("%Y-%m-%d")
            except ValueError:
                continue

    return str(value) if value else None


def _safe_float(value, default: float = 0.0) -> float:
    """Safely convert value to float, handling comma-formatted numbers."""
    if pd.isna(value):
        return default
    try:
        if isinstance(value, str):
            # Remove commas and handle accounting format (parentheses for negatives)
            cleaned = value.replace(",", "").strip()
            if cleaned.startswith("(") and cleaned.endswith(")"):
                cleaned = "-" + cleaned[1:-1]
            return float(cleaned)
        return float(value)
    except (ValueError, TypeError):
        return default


def _safe_int(value, default: int = 0) -> int:
    """Safely convert value to int, handling comma-formatted numbers."""
    if pd.isna(value):
        return default
    try:
        if isinstance(value, str):
            # Remove commas
            cleaned = value.replace(",", "").strip()
            return int(float(cleaned))
        return int(float(value))
    except (ValueError, TypeError):
        return default


