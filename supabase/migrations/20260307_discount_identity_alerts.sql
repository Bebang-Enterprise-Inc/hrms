-- ============================================================================
-- Migration: Discount identity same-day alerts
-- Date: 2026-03-07
-- Why: Flag repeated Senior Citizen / PWD names and reference numbers directly
--      in Supabase so audit can review same-day reuse within one store or
--      across multiple stores without relying on external scripts.
-- Notes:
--   - Counts distinct orders, not item rows.
--   - Collapses duplicate item lines within the same receipt to one identity
--     row per order/category/name/reference combination.
--   - Reuses public.discount_abuse_alerts and its unique key.
--   - Scheduling is intentionally left for a separate approval step.
-- ============================================================================

