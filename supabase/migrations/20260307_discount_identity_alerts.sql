create or replace view public.v_discount_identity_order_usage as
with normalized_source as (
  select
    po.business_date,
    po.location_id,
    coalesce(s.store_name, 'Location ' || po.location_id::text) as store_name,
    po.id as order_id,
    po.bill_number,
    po.receipt_number,
    po.billed_at,
    po.paid_at,
    case
      when upper(coalesce(poi.discount_bir_category, '')) in ('SC', 'PWD')
        then upper(poi.discount_bir_category)
      when upper(coalesce(poi.discount_name_normalized, poi.discount_name, '')) like '%SENIOR%'
        then 'SC'
      when upper(coalesce(poi.discount_name_normalized, poi.discount_name, '')) like '%PWD%'
        then 'PWD'
      else null
    end as discount_bir_category,
    upper(
      nullif(
        trim(
          regexp_replace(
            coalesce(
              nullif(poi.discount_customer_full_name_normalized, ''),
              nullif(poi.discount_customer_full_name, ''),
              nullif(trim(concat_ws(' ', poi.discount_customer_first_name, poi.discount_customer_last_name)), '')
            ),
            '\s+',
            ' ',
            'g'
          )
        ),
        ''
      )
    ) as full_name_normalized,
    upper(
      nullif(
        regexp_replace(
          coalesce(
            nullif(poi.discount_reference_number_normalized, ''),
            nullif(trim(poi.discount_reference_number), ''),
            ''
          ),
          '[^A-Za-z0-9]+',
          '',
          'g'
        ),
        ''
      )
    ) as reference_number_normalized,
    coalesce(
      nullif(trim(poi.discount_customer_full_name), ''),
      nullif(trim(concat_ws(' ', poi.discount_customer_first_name, poi.discount_customer_last_name)), '')
    ) as customer_name_display,
    coalesce(
      nullif(trim(poi.discount_reference_number), ''),
      nullif(trim(poi.discount_reference_number_normalized), '')
    ) as reference_number_display,
    upper(
      nullif(
        trim(
          regexp_replace(
            coalesce(poi.discount_name_normalized, poi.discount_name, ''),
            '\s+',
            ' ',
            'g'
          )
        ),
        ''
      )
    ) as discount_name_normalized,
    coalesce(poi.discount_amount, 0)::numeric as discount_amount
  from public.pos_order_items poi
  join public.pos_orders po
    on po.id = poi.order_id
  left join public.stores s
    on s.location_id = po.location_id
  where po.payment_status = 'PAID'
),
identity_usage as (
  select
    business_date,
    location_id,
    store_name,
    order_id,
    bill_number,
    receipt_number,
    billed_at,
    paid_at,
    discount_bir_category,
    full_name_normalized,
    reference_number_normalized,
    max(customer_name_display) as customer_name_display,
    max(reference_number_display) as reference_number_display,
    max(discount_name_normalized) as discount_name_normalized,
    round(sum(discount_amount)::numeric, 2) as discount_amount_total
  from normalized_source
  where discount_bir_category is not null
    and (full_name_normalized is not null or reference_number_normalized is not null)
  group by
    business_date,
    location_id,
    store_name,
    order_id,
    bill_number,
    receipt_number,
    billed_at,
    paid_at,
    discount_bir_category,
    full_name_normalized,
    reference_number_normalized
)
select
  business_date,
  location_id,
  store_name,
  order_id,
  bill_number,
  receipt_number,
  billed_at,
  paid_at,
  discount_bir_category,
  coalesce(
    discount_name_normalized,
    case discount_bir_category
      when 'SC' then 'SENIOR CITIZEN DISCOUNT'
      when 'PWD' then 'PWD'
      else discount_bir_category
    end
  ) as discount_name_normalized,
  full_name_normalized,
  reference_number_normalized,
  customer_name_display,
  reference_number_display,
  discount_amount_total
from identity_usage;

