create or replace function public.refresh_discount_identity_alerts(
  p_business_date date default null,
  p_categories text[] default array['SC']
)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_categories text[];
  v_detection_types constant text[] := array[
    'same_name_same_day_same_store',
    'same_name_same_day_multi_store',
    'same_name_diff_reference_same_day_same_store',
    'same_name_diff_reference_same_day_multi_store',
    'same_reference_same_day_same_store',
    'same_reference_same_day_multi_store',
    'same_reference_diff_name_same_day_same_store',
    'same_reference_diff_name_same_day_multi_store'
  ];
  v_alert_count integer := 0;
begin
  select coalesce(array_agg(category), array[]::text[])
    into v_categories
  from (
    select distinct upper(trim(x)) as category
    from unnest(coalesce(p_categories, array['SC'])) as t(x)
    where nullif(trim(x), '') is not null
  ) normalized_categories;

  if cardinality(v_categories) = 0 then
    raise exception 'refresh_discount_identity_alerts(): p_categories must contain at least one non-empty value';
  end if;

  create temp table tmp_discount_identity_alerts on commit drop as
  with base as (
    select
      business_date,
      location_id,
      store_name,
      order_id,
      bill_number,
      receipt_number,
      billed_at,
      paid_at,
      discount_bir_category,
      discount_name_normalized,
      full_name_normalized,
      reference_number_normalized,
      coalesce(customer_name_display, full_name_normalized) as customer_name_display,
      coalesce(reference_number_display, reference_number_normalized) as reference_number_display,
      discount_amount_total
    from public.v_discount_identity_order_usage
    where discount_bir_category = any (v_categories)
      and (p_business_date is null or business_date = p_business_date)
  ),
  name_store_ranked as (
    select
      b.*,
      lead(billed_at) over (
        partition by business_date, location_id, discount_bir_category, full_name_normalized
        order by billed_at nulls last, order_id
      ) as next_billed_at
    from base b
    where full_name_normalized is not null
  ),
  name_store_groups as (
    select
      business_date,
      location_id,
      max(store_name) as store_name,
      discount_bir_category,
      full_name_normalized as identity_key,
      max(discount_name_normalized) as discount_name,
      array_agg(distinct customer_name_display order by customer_name_display)
        filter (where customer_name_display is not null) as customer_names,
      array_agg(distinct reference_number_display order by reference_number_display)
        filter (where reference_number_display is not null) as reference_numbers,
      array_agg(distinct reference_number_normalized order by reference_number_normalized)
        filter (where reference_number_normalized is not null) as reference_keys,
      array_agg(distinct order_id order by order_id) as order_ids,
      array_agg(distinct bill_number::text order by bill_number::text)
        filter (where bill_number is not null) as bill_numbers,
      array_agg(distinct receipt_number::text order by receipt_number::text)
        filter (where receipt_number is not null) as receipt_numbers,
      count(distinct order_id)::integer as order_count,
      count(distinct reference_number_normalized)::integer as distinct_reference_count,
      min(billed_at) as first_billed_at,
      max(billed_at) as last_billed_at,
      min(paid_at) as first_paid_at,
      max(paid_at) as last_paid_at,
      min(round((extract(epoch from (next_billed_at - billed_at)) / 60.0)::numeric, 1))
        filter (where billed_at is not null and next_billed_at is not null) as min_gap_minutes,
      round(sum(discount_amount_total)::numeric, 2) as discount_amount_total
    from name_store_ranked
    group by business_date, location_id, discount_bir_category, full_name_normalized
    having count(distinct order_id) >= 2
  ),
  name_multi_store_ranked as (
    select
      b.*,
      lead(billed_at) over (
        partition by business_date, discount_bir_category, full_name_normalized
        order by billed_at nulls last, order_id
      ) as next_billed_at
    from base b
    where full_name_normalized is not null
  ),
  name_multi_store_groups as (
    select
      business_date,
      discount_bir_category,
      full_name_normalized as identity_key,
      max(discount_name_normalized) as discount_name,
      array_agg(distinct location_id order by location_id) as location_ids,
      array_agg(distinct store_name order by store_name)
        filter (where store_name is not null) as store_names,
      array_agg(distinct customer_name_display order by customer_name_display)
        filter (where customer_name_display is not null) as customer_names,
      array_agg(distinct reference_number_display order by reference_number_display)
        filter (where reference_number_display is not null) as reference_numbers,
      array_agg(distinct reference_number_normalized order by reference_number_normalized)
        filter (where reference_number_normalized is not null) as reference_keys,
      array_agg(distinct order_id order by order_id) as order_ids,
      array_agg(distinct bill_number::text order by bill_number::text)
        filter (where bill_number is not null) as bill_numbers,
      count(distinct order_id)::integer as order_count,
      count(distinct location_id)::integer as store_count,
      count(distinct reference_number_normalized)::integer as distinct_reference_count,
      min(billed_at) as first_billed_at,
      max(billed_at) as last_billed_at,
      min(paid_at) as first_paid_at,
      max(paid_at) as last_paid_at,
      min(round((extract(epoch from (next_billed_at - billed_at)) / 60.0)::numeric, 1))
        filter (where billed_at is not null and next_billed_at is not null) as min_gap_minutes,
      round(sum(discount_amount_total)::numeric, 2) as discount_amount_total
    from name_multi_store_ranked
    group by business_date, discount_bir_category, full_name_normalized
    having count(distinct location_id) >= 2
       and count(distinct order_id) >= 2
  ),
  ref_store_ranked as (
    select
      b.*,
      lead(billed_at) over (
        partition by business_date, location_id, discount_bir_category, reference_number_normalized
        order by billed_at nulls last, order_id
      ) as next_billed_at
    from base b
    where reference_number_normalized is not null
  ),
  ref_store_groups as (
