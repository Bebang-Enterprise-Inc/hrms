      active_day_count,
      distinct_counterparty_count,
      'rolling_30d_name_diff_reference_threshold_same_store'::text as detection_type,
      'high'::text as severity,
      details || jsonb_build_object(
        'window_start', window_start,
        'window_end', window_end,
        'active_day_count', active_day_count,
        'distinct_counterparty_count', distinct_counterparty_count,
        'threshold_order_count', p_name_order_threshold,
        'comparison_operator', '>'
      ) as details
    from (
      select
        window_start,
        window_end,
        location_id,
        store_name,
        discount_name,
        discount_bir_category,
        'full_name'::text as identity_type,
        identity_key,
        customer_names[1] as customer_name,
        order_count,
        active_day_count,
        distinct_counterparty_count,
        details
      from name_usage
    ) usage_base
    where identity_type = 'full_name'
      and order_count > p_name_order_threshold
      and distinct_counterparty_count > 1

    union all

    select
      v_as_of_date as as_of_date,
      window_start,
      window_end,
      'store_30d'::text as scope,
      location_id as scope_key,
      location_id,
      store_name,
      discount_name,
      discount_bir_category,
      identity_type,
      identity_key,
      customer_name,
      reference_number,
      order_count,
      active_day_count,
      distinct_counterparty_count,
      'rolling_30d_reference_threshold_same_store'::text as detection_type,
      'review'::text as severity,
      details || jsonb_build_object(
        'window_start', window_start,
        'window_end', window_end,
        'active_day_count', active_day_count,
        'distinct_counterparty_count', distinct_counterparty_count,
        'threshold_order_count', p_reference_order_threshold,
        'comparison_operator', '>'
      ) as details
    from (
      select
        window_start,
        window_end,
        location_id,
        store_name,
        discount_name,
        discount_bir_category,
        'reference_number'::text as identity_type,
        identity_key,
        case when distinct_counterparty_count = 1 then customer_names[1] else null end as customer_name,
        reference_numbers[1] as reference_number,
        order_count,
        active_day_count,
        distinct_counterparty_count,
        details
      from reference_usage
    ) usage_base
    where identity_type = 'reference_number'
      and order_count > p_reference_order_threshold

    union all

    select
      v_as_of_date as as_of_date,
      window_start,
      window_end,
      'store_30d'::text as scope,
      location_id as scope_key,
      location_id,
      store_name,
      discount_name,
      discount_bir_category,
      identity_type,
      identity_key,
      null::text as customer_name,
      reference_number,
      order_count,
      active_day_count,
      distinct_counterparty_count,
      'rolling_30d_reference_diff_name_threshold_same_store'::text as detection_type,
      'high'::text as severity,
      details || jsonb_build_object(
        'window_start', window_start,
        'window_end', window_end,
        'active_day_count', active_day_count,
        'distinct_counterparty_count', distinct_counterparty_count,
        'threshold_order_count', p_reference_order_threshold,
        'comparison_operator', '>'
      ) as details
    from (
      select
        window_start,
        window_end,
        location_id,
        store_name,
        discount_name,
        discount_bir_category,
        'reference_number'::text as identity_type,
        identity_key,
        reference_numbers[1] as reference_number,
        order_count,
        active_day_count,
        distinct_counterparty_count,
        details
      from reference_usage
    ) usage_base
    where identity_type = 'reference_number'
      and order_count > p_reference_order_threshold
      and distinct_counterparty_count > 1
  )
  select *
  from computed_alerts;

  delete from public.discount_identity_30d_snapshots existing
  where existing.as_of_date = v_as_of_date
    and existing.detection_type = any (v_detection_types)
    and existing.discount_bir_category = any (v_categories)
    and not exists (
      select 1
      from tmp_discount_rolling_30d_snapshots fresh
      where fresh.as_of_date = existing.as_of_date
        and fresh.scope = existing.scope
        and fresh.scope_key = existing.scope_key
        and fresh.discount_bir_category = existing.discount_bir_category
        and fresh.identity_type = existing.identity_type
        and fresh.identity_key = existing.identity_key
        and fresh.detection_type = existing.detection_type
    );

  insert into public.discount_identity_30d_snapshots (
    as_of_date,
    window_start,
    window_end,
    scope,
    scope_key,
    location_id,
    store_name,
    discount_name,
    discount_bir_category,
    identity_type,
    identity_key,
    customer_name,
    reference_number,
    order_count,
    active_day_count,
    distinct_counterparty_count,
    detection_type,
    severity,
    details,
    created_at,
    updated_at
  )
  select
    as_of_date,
    window_start,
    window_end,
    scope,
    scope_key,
    location_id,
    store_name,
    discount_name,
    discount_bir_category,
    identity_type,
    identity_key,
    customer_name,
    reference_number,
    order_count,
    active_day_count,
    distinct_counterparty_count,
    detection_type,
    severity,
    details,
    now() as created_at,
    now() as updated_at
  from tmp_discount_rolling_30d_snapshots
  on conflict (
    as_of_date,
    scope,
    scope_key,
    discount_bir_category,
    identity_type,
    identity_key,
    detection_type
  )
  do update
    set location_id = excluded.location_id,
        store_name = excluded.store_name,
        discount_name = excluded.discount_name,
        customer_name = excluded.customer_name,
        reference_number = excluded.reference_number,
        window_start = excluded.window_start,
        window_end = excluded.window_end,
        order_count = excluded.order_count,
        active_day_count = excluded.active_day_count,
        distinct_counterparty_count = excluded.distinct_counterparty_count,
        severity = excluded.severity,
        details = excluded.details,
        updated_at = now();

  select count(*)
    into v_alert_count
  from tmp_discount_rolling_30d_snapshots;

  return v_alert_count;
end;
