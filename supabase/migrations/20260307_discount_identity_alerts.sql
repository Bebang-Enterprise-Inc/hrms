      round(sum(discount_amount_total)::numeric, 2) as discount_amount_total
    from ref_store_ranked
    group by business_date, location_id, discount_bir_category, reference_number_normalized
    having count(distinct order_id) >= 2
  ),
  ref_multi_store_ranked as (
    select
      b.*,
      lead(billed_at) over (
        partition by business_date, discount_bir_category, reference_number_normalized
        order by billed_at nulls last, order_id
      ) as next_billed_at
    from base b
    where reference_number_normalized is not null
  ),
  ref_multi_store_groups as (
    select
      business_date,
      discount_bir_category,
      reference_number_normalized as identity_key,
      max(discount_name_normalized) as discount_name,
      array_agg(distinct location_id order by location_id) as location_ids,
      array_agg(distinct store_name order by store_name)
        filter (where store_name is not null) as store_names,
      array_agg(distinct customer_name_display order by customer_name_display)
        filter (where customer_name_display is not null) as customer_names,
      array_agg(distinct reference_number_display order by reference_number_display)
        filter (where reference_number_display is not null) as reference_numbers,
      array_agg(distinct full_name_normalized order by full_name_normalized)
        filter (where full_name_normalized is not null) as name_keys,
      array_agg(distinct order_id order by order_id) as order_ids,
      array_agg(distinct bill_number::text order by bill_number::text)
        filter (where bill_number is not null) as bill_numbers,
      count(distinct order_id)::integer as order_count,
      count(distinct location_id)::integer as store_count,
      count(distinct full_name_normalized)::integer as distinct_name_count,
      min(billed_at) as first_billed_at,
      max(billed_at) as last_billed_at,
      min(paid_at) as first_paid_at,
      max(paid_at) as last_paid_at,
